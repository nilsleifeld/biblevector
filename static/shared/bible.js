/**
 * @param {number} book
 */
export function getBookDetails(book) {
  return books[book - 1];
}

/**
 * @param {number} book
 * @param {number} chapter
 */
export function chapterKeyShort(book, chapter) {
  const details = getBookDetails(book);
  return `${details.abbreviation} ${chapter}`;
}

/**
 * @param {number} book
 * @param {number} chapter
 */
export function chapterKey(book, chapter) {
  const details = getBookDetails(book);
  return `${details.name} ${chapter}`;
}

/**
 * @param {number} book
 */
export function isLastBook(book) {
  return book === books.length;
}

/**
 * @param {number} book
 * @param {number} chapter
 */
export function canOpenNextChapter(book, chapter) {
  if (!isLastBook(book)) {
    return true;
  }
  const bookDetails = getBookDetails(book);
  return chapter !== bookDetails.chapters;
}
